From mathcomp Require Import fingroup.
Open Scope group_scope.
(* Note: This formalization only finite groups (due to mathcomp), but this is sufficiently general since the group generated by A and B is finite. *)
Variable T : finGroupType.
Theorem putnam_1972_b3
    (G : {group T})
    (A B : T)
    (hab : A * B * A = B * A ^+ 2 * B /\ A ^+ 3 = 1 /\ exists n : nat, n > 0 /\ B ^+ (2*n - 1) = 1)
    : (B = 1).
Proof. Admitted.